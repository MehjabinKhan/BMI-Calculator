"use strict";
// BMI Calculator
// Calculating Self BMI
let weightInKg = 40;
let heightInMeters = 4.5;
let bmi = weightInKg / (heightInMeters * heightInMeters);
console.log(`Your BMI is ${bmi}.`);
// Calculating My Father BMI
let weightInKg1 = 70;
let heightInMeters1 = 5.2;
let bmi1 = weightInKg1 / (heightInMeters1 * heightInMeters1);
console.log(`Your Father BMI is ${bmi1}.`);
// Calculating Brother BMI
let weightInKg2 = 50;
let heightInMeters2 = 5.5;
let bmi2 = weightInKg2 / (heightInMeters2 * heightInMeters2);
